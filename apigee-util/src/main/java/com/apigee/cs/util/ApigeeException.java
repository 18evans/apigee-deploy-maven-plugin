package com.apigee.cs.util;

public class ApigeeException extends RuntimeException {

	public ApigeeException() {
		super();
		
	}

	public ApigeeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public ApigeeException(String arg0) {
		super(arg0);
		
	}

	public ApigeeException(Throwable arg0) {
		super(arg0);
		
	}

}
